package com.testing.class2;

public class self {
    public static void main(String[] args) {
        //自增 ++  自减 --
        int a=3;
        int b=4;
        a++;
        System.out.println("后自增之后的值" + a);
        ++a;
        System.out.println("前自增之后的值" + a);
        b--;
        System.out.println("后自减之后的值" + b);
        --b;
        System.out.println("前自减之后的值" + b);
    }
}
